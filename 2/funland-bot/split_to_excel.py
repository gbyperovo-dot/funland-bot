# split_to_excel.py
import pandas as pd

# –ò—Å—Ö–æ–¥–Ω—ã–π —Ç–µ–∫—Å—Ç (–º–æ–∂–Ω–æ –∑–∞–≥—Ä—É–∑–∏—Ç—å –∏–∑ .txt)
data = """
–ø—Ä–∏–≤–µ—Ç|üëã –ü—Ä–∏–≤–µ—Ç! –†–∞–¥ –≤–∞—Å –≤–∏–¥–µ—Ç—å –≤ FunLand! üòä
–º–µ–Ω—é|üè† –ì–ª–∞–≤–Ω–æ–µ –º–µ–Ω—é
vr|üéÆ VR-–∑–æ–Ω—ã
–±–∞—Ç—É—Ç—ã|üèÄ –ë–∞—Ç—É—Ç–Ω—ã–π —Ü–µ–Ω—Ç—Ä
–Ω–µ—Ä—Ñ|üî´ –ù–µ—Ä—Ñ-–∞—Ä–µ–Ω–∞
"""

# –†–∞–∑–¥–µ–ª—è–µ–º —Å—Ç—Ä–æ–∫–∏ –∏ –ø–∞—Ä—Å–∏–º
rows = []
for line in data.strip().split('\n'):
    if '|' in line:
        key, value = line.split('|', 1)  # 1 ‚Äî —Ç–æ–ª—å–∫–æ –ø–µ—Ä–≤–æ–µ —Ä–∞–∑–¥–µ–ª–µ–Ω–∏–µ
        rows.append({"key (–≤–æ–ø—Ä–æ—Å)": key.strip(), "value (–æ—Ç–≤–µ—Ç)": value.strip()})

# –°–æ—Ö—Ä–∞–Ω—è–µ–º –≤ Excel
df = pd.DataFrame(rows)
df.to_excel("knowledge_base.xlsx", index=False)
print("‚úÖ –ì–æ—Ç–æ–≤–æ! –§–∞–π–ª —Å–æ—Ö—Ä–∞–Ω—ë–Ω –∫–∞–∫ knowledge_base.xlsx")